create or replace function public.muller_is_creator(p_user uuid)
returns boolean
language sql
security definer
set search_path = public
as $$
  select exists(select 1 from public.muller_creator_accounts c where c.user_id = p_user);
$$;

create or replace function public.muller_get_wallet()
returns table (coins bigint, is_creator boolean)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_uid uuid;
begin
  v_uid := auth.uid();
  if v_uid is null then
    raise exception 'NOT_AUTHENTICATED';
  end if;

  insert into public.muller_wallets (user_id, coins, updated_at)
  values (v_uid, 0, now())
  on conflict (user_id) do nothing;

  return query
    select
      case
        when public.muller_is_creator(v_uid) then 999999999::bigint
        else coalesce((select w.coins from public.muller_wallets w where w.user_id = v_uid), 0)::bigint
      end as coins,
      public.muller_is_creator(v_uid) as is_creator;
end;
$$;

create or replace function public.muller_claim_reward(p_reward_type text)
returns table (granted int, balance bigint, reason text)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_uid uuid;
  v_day text := to_char(now(), 'YYYY-MM-DD');
  v_now timestamptz := now();
  v_claims_today int := 0;
  v_last_claim timestamptz := null;
begin
  v_uid := auth.uid();
  if v_uid is null then
    raise exception 'NOT_AUTHENTICATED';
  end if;

  insert into public.muller_wallets (user_id, coins, updated_at)
  values (v_uid, 0, now())
  on conflict (user_id) do nothing;

  if public.muller_is_creator(v_uid) then
    granted := 0;
    balance := 999999999::bigint;
    reason := 'creator_unlimited';
    return next;
    return;
  end if;

  if p_reward_type not in ('daily_bonus', 'ad_reward') then
    granted := 0;
    select coins into balance from public.muller_wallets where user_id = v_uid;
    balance := coalesce(balance, 0);
    reason := 'invalid_reward_type';
    return next;
    return;
  end if;

  select claim_count, last_claim_at
    into v_claims_today, v_last_claim
    from public.muller_reward_claims
    where user_id = v_uid and reward_type = p_reward_type and day_key = v_day;

  v_claims_today := coalesce(v_claims_today, 0);
  granted := 0;
  reason := 'ok';

  if p_reward_type = 'daily_bonus' then
    if v_claims_today >= 1 then
      reason := 'already_claimed_today';
    else
      granted := 40;
    end if;
  elsif p_reward_type = 'ad_reward' then
    if v_claims_today >= 6 then
      reason := 'daily_limit_reached';
    elsif v_last_claim is not null and v_last_claim > (v_now - interval '15 minutes') then
      reason := 'cooldown_15m';
    else
      granted := 18;
    end if;
  end if;

  if granted > 0 then
    update public.muller_wallets
      set coins = coins + granted,
          updated_at = v_now
      where user_id = v_uid
      returning coins into balance;

    insert into public.muller_reward_claims (user_id, reward_type, day_key, claim_count, last_claim_at)
    values (v_uid, p_reward_type, v_day, 1, v_now)
    on conflict (user_id, reward_type, day_key)
    do update set claim_count = public.muller_reward_claims.claim_count + 1,
                  last_claim_at = excluded.last_claim_at;
    reason := 'ok';
  else
    select coins into balance from public.muller_wallets where user_id = v_uid;
  end if;

  balance := coalesce(balance, 0);
  return next;
  return;
end;
$$;

create or replace function public.muller_spend_coins(p_amount int, p_reason text default 'spend')
returns table (ok boolean, balance bigint, reason text)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_uid uuid;
  v_is_creator boolean := false;
  v_balance bigint := 0;
begin
  v_uid := auth.uid();
  if v_uid is null then
    raise exception 'NOT_AUTHENTICATED';
  end if;

  if p_amount is null or p_amount <= 0 then
    return query select false, 0::bigint, 'invalid_amount';
    return;
  end if;

  insert into public.muller_wallets (user_id, coins, updated_at)
  values (v_uid, 0, now())
  on conflict (user_id) do nothing;

  v_is_creator := public.muller_is_creator(v_uid);
  if v_is_creator then
    return query select true, 999999999::bigint, 'creator_unlimited';
    return;
  end if;

  update public.muller_wallets
    set coins = coins - p_amount,
        updated_at = now()
    where user_id = v_uid
      and coins >= p_amount
    returning coins into v_balance;

  if v_balance is null then
    select coins into v_balance from public.muller_wallets where user_id = v_uid;
    return query select false, coalesce(v_balance, 0), 'insufficient_coins';
    return;
  end if;

  return query select true, v_balance, coalesce(p_reason, 'ok');
end;
$$;

create or replace function public.muller_submit_league_score(p_week_key text, p_score int, p_display_name text)
returns table (saved_score int)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_uid uuid;
  v_score int;
begin
  v_uid := auth.uid();
  if v_uid is null then
    raise exception 'NOT_AUTHENTICATED';
  end if;

  v_score := greatest(0, least(coalesce(p_score, 0), 50000));

  insert into public.league_scores (user_id, week_key, score, display_name, updated_at)
  values (v_uid, coalesce(p_week_key, to_char(now(), 'YYYY-MM-DD')), v_score, left(coalesce(p_display_name, 'Estudiante'), 80), now())
  on conflict (user_id, week_key)
  do update set score = greatest(public.league_scores.score, excluded.score),
                display_name = excluded.display_name,
                updated_at = now();

  return query
    select score from public.league_scores where user_id = v_uid and week_key = coalesce(p_week_key, to_char(now(), 'YYYY-MM-DD'));
end;
$$;

